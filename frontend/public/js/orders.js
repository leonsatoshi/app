/**
 * NOVA — Orders Module
 * Builds, signs, and submits CLOB orders.
 * Handles: L2 auth headers, order structure, sim intercept, error recovery.
 */

import { CHAIN_ID } from './constants.js';
import { PM, SIM, CFG, S } from './state.js';
import { buildL2Headers, toChecksumAddress } from './auth.js';
import { postOrder, fetchOpenOrders } from './api.js';
import { getSafeNonce, delay } from './utils.js';

// ── Submit Order ───────────────────────────────────────────────────────────
// Main entry point. Intercepts to sim engine if sim mode is on.
export async function submitOrder({ market, side, amountUSD, orderType = 'market', _nonce }) {
  // Sim intercept
  if (SIM.enabled) {
    return submitSimOrder({ market, side, amountUSD, _nonce });
  }

  // Validation
  if (!PM.connected) throw new Error('Wallet not connected');
  if (!PM.hasL2)     throw new Error('Not authorized — click ⚡ Authorize first');

  const makerAddress = PM.makerAddress;
  if (!makerAddress) throw new Error('No maker address available');

  // Balance check
  // R-ORDER-01: read from S.wallet.balance — window._novaBalance was never set
  // R-NET-03:   null balance means RPC was unreachable, not $0 — block with a clear message
  // Note: SIM.enabled is always false here — the sim early-return above catches it first.
  const rawBalance = S.wallet?.balance;
  if (rawBalance == null) throw new Error('Balance unavailable — Polygon RPC unreachable. Retry in a moment.');
  const balance = parseFloat(rawBalance);
  if (amountUSD > balance) throw new Error(`Insufficient balance: $${balance.toFixed(2)} available`);

  // R-ORDER-05: enforce trading safety toggles — these are real guardrails, not just UI hints
  if (!CFG.tradingEnabled) throw new Error('Live trading is disabled — enable it in Settings');
  if (amountUSD > CFG.maxPositionUSD) throw new Error(`Order size $${amountUSD.toFixed(2)} exceeds max position limit ($${CFG.maxPositionUSD}). Raise it in Settings if intended.`);

  return submitLiveOrder({ market, side, amountUSD, orderType, makerAddress, _nonce });
}

// ── Live CLOB Order ────────────────────────────────────────────────────────
async function submitLiveOrder({ market, side, amountUSD, orderType, makerAddress, _nonce }) {
  const tokenId = extractTokenId(market, side);
  if (!tokenId) throw new Error('Could not determine token ID for this market/side');

  // R-ORDER-02: never default price to 0.5 — a wrong price produces a wrong share count
  const price = side === 'YES' ? market.yesPrice : market.noPrice;
  if (!price || price <= 0) throw new Error('Market price unavailable — refresh markets before trading.');
  const shares = amountUSD / price;

  // Use caller-supplied nonce if provided (e.g. pre-generated by deployCapital before
  // concurrent dispatch). Fall back to getSafeNonce() for single-order paths.
  const nonce = _nonce ?? getSafeNonce();

  // salt: order uniqueness identifier — must be a random uint256 per order.
  // All order fields submitted to the CLOB must be JSON strings, not numbers.
  // crypto.getRandomValues returns a number — explicitly toString() it here
  // to match makerAmount, takerAmount, nonce, etc. which are all strings.
  // Sending salt as a JSON number when the CLOB expects a string returns 422.
  const salt = crypto.getRandomValues(new Uint32Array(1))[0].toString();

  // R-ORDER-06: signatureType must match wallet configuration.
  // Polymarket uses three signature modes:
  //   '0' = EOA         — maker IS the signer. No proxy wallet. Pure EOA accounts only.
  //   '1' = Gnosis Safe — multi-sig smart contract wallet.
  //   '2' = POLY_PROXY  — maker is the Polymarket proxy wallet (holds USDC),
  //                        signer is the EOA. This is the standard case for all
  //                        Polymarket users who have deposited funds, because
  //                        Polymarket creates a proxy contract wallet for every EOA.
  // Using type 0 for a proxy-wallet user → ecrecover gets wrong address → 401.
  const signatureType = PM.proxyAddress ? '2' : '0';

  const order = {
    salt:          salt,
    maker:         toChecksumAddress(makerAddress),       // proxy wallet — holds USDC
    signer:        toChecksumAddress(PM.address),         // EOA — the address that actually signs
    taker:         '0x0000000000000000000000000000000000000000',
    tokenId,
    makerAmount:   Math.round(amountUSD * 1_000_000).toString(), // USDC micro-units
    takerAmount:   Math.round(shares   * 1_000_000).toString(),
    expiration:    '0',
    nonce:         nonce.toString(),
    feeRateBps:    '0',
    side:          side === 'YES' ? '0' : '1',
    signatureType,
  };

  // Build EIP-712 typed data for the order
  const domain = {
    name:              'Polymarket CTF Exchange',
    version:           '1',
    chainId:           CHAIN_ID,
    verifyingContract: '0x4bFb41d5B3570DeFd03C39a9A4D8dE6Bd8B8982E',
  };

  const types = {
    Order: [
      { name: 'salt',          type: 'uint256' },
      { name: 'maker',         type: 'address' },
      { name: 'signer',        type: 'address' },
      { name: 'taker',         type: 'address' },
      { name: 'tokenId',       type: 'uint256' },
      { name: 'makerAmount',   type: 'uint256' },
      { name: 'takerAmount',   type: 'uint256' },
      { name: 'expiration',    type: 'uint256' },
      { name: 'nonce',         type: 'uint256' },
      { name: 'feeRateBps',    type: 'uint256' },
      { name: 'side',          type: 'uint8'   },
      { name: 'signatureType', type: 'uint8'   },
    ],
  };

  // Sign order
  let signature;
  try {
    signature = await PM.provider.request({
      method: 'eth_signTypedData_v4',
      params: [PM.address, JSON.stringify({ domain, types, primaryType: 'Order', message: order })],
    });
  } catch (err) {
    throw new Error('Order signing rejected: ' + err.message);
  }

  const signedOrder = { ...order, signature };

  // R-ORDER-07: map internal orderType to CLOB order type strings.
  // The CLOB API requires an explicit orderType in the request wrapper body.
  // 'market' → 'FOK' (Fill or Kill): executes immediately at best available price
  //            or cancels the whole order. This is true market-order semantics.
  // 'limit'  → 'GTC' (Good Till Cancelled): rests on the book at the specified price.
  // 'fak'    → 'FAK' (Fill and Kill): fills as much as possible, cancels the rest.
  // NOT passing orderType at all, or passing the internal string directly,
  // causes the CLOB to return 400/422.
  const CLOB_ORDER_TYPES = {
    market: 'FOK',
    limit:  'GTC',
    fok:    'FOK',
    fak:    'FAK',
    gtc:    'GTC',
  };
  const clobOrderType = CLOB_ORDER_TYPES[orderType?.toLowerCase()] ?? 'GTC';

  // R-ORDER-08: POST /order requires a wrapper body, not the raw signed order.
  // Polymarket's CLOB REST spec requires:
  //   { order: <signedOrder>, owner: <makerAddress>, orderType: <"GTC"|"FOK"|"FAK"> }
  // Sending signedOrder directly → 400 (missing required fields 'owner', 'orderType').
  const orderWrapper = {
    order:     signedOrder,
    owner:     toChecksumAddress(makerAddress),
    orderType: clobOrderType,
  };

  // Build L2 auth headers.
  // CRITICAL: the HMAC signs the exact bytes sent in the HTTP body.
  // Must stringify the wrapper (not just signedOrder) — body mismatch → auth failure.
  const orderBody = JSON.stringify(orderWrapper);
  const l2Headers = await buildL2Headers('POST', '/order', orderBody).catch(err => {
    throw new Error('Failed to build auth headers: ' + err.message);
  });

  // Submit to CLOB
  const result = await postOrder(orderWrapper, l2Headers);

  if (!result.ok) {
    // R-ORDER-03: on timeout (status 0), check if the order actually landed before
    // reporting failure — a network blip can abort the fetch after the server received it.
    if (result.status === 0 && result.error === 'Request timed out') {
      console.warn('[NOVA] Order timed out — checking if it landed on the CLOB…');
      try {
        const openOrders = await fetchOpenOrders(l2Headers);
        if (openOrders.ok && Array.isArray(openOrders.data)) {
          const maybeOurs = openOrders.data.find(o =>
            o.asset_id === tokenId && o.side === (side === 'YES' ? 'BUY' : 'SELL')
          );
          if (maybeOurs) {
            console.log('[NOVA] ⚠ Order found on CLOB despite timeout — treating as submitted');
            window.showToast?.('Order may have landed — check Open Orders in sidebar', 'warn');
            return { ok: true, data: maybeOurs, order: orderWrapper, timedOut: true };
          }
        }
      } catch (e) {
        console.warn('[NOVA] Could not verify order status after timeout:', e.message);
      }
      throw new Error('Order timed out. Check Open Orders in the sidebar to confirm status.');
    }

    const errMsg = parseOrderError(result);
    throw new Error(errMsg);
  }

  console.log('[NOVA] ✓ Order submitted:', result.data);
  return { ok: true, data: result.data, order: orderWrapper };
}

// ── Sim Order ─────────────────────────────────────────────────────────────
async function submitSimOrder({ market, side, amountUSD, _nonce: _unused }) {
  await delay(600 + Math.random() * 400); // Realistic latency

  // Random 10% failure rate for realism
  if (Math.random() < 0.1) {
    SIM.log.push({ ts: Date.now(), type: 'error', msg: `SIM: Order rejected (simulated failure) — $${amountUSD} ${side}` });
    throw new Error('SIM: Order rejected (simulated exchange rejection)');
  }

  SIM.balance -= amountUSD;
  const tradeId = 'SIM-' + Date.now().toString(36).toUpperCase();

  SIM.orders.push({
    id:       tradeId,
    market:   market.question || 'Unknown',
    side,
    amount:   amountUSD,
    price:    side === 'YES' ? market.yesPrice : market.noPrice,
    ts:       Date.now(),
    status:   'filled',
  });

  const fillPrice = (side === 'YES' ? market.yesPrice : market.noPrice) || 0;
  SIM.log.push({
    ts:   Date.now(),
    type: 'ok',
    msg:  `SIM: ${side} $${amountUSD.toFixed(2)} filled at ${fillPrice.toFixed(3)} — ${tradeId}`,
  });

  return { ok: true, simulated: true, tradeId };
}

// ── Helpers ────────────────────────────────────────────────────────────────
// R-ORDER-04: conditionId is NOT a CLOB token ID — removed that fallback.
// If clobTokenIds is missing the market data is incomplete; surface that clearly.
function extractTokenId(market, side) {
  const outcomes = market.outcomes || [];
  const tokens   = market.clobTokenIds || market.tokens || [];

  // Primary: clobTokenIds array (index 0 = YES, 1 = NO)
  if (tokens.length >= 2) {
    return side === 'YES' ? tokens[0] : tokens[1];
  }

  // Secondary: match by outcomes label
  const idx = outcomes.findIndex(o => o.toUpperCase() === side);
  if (idx >= 0 && tokens[idx]) return tokens[idx];

  // No fallback to conditionId — wrong type, silently wrong token
  return null;
}

function parseOrderError(result) {
  if (result.status === 401) return 'Auth rejected — re-authorize with ⚡ button';
  if (result.status === 403) return 'Forbidden — check your region or token allowances';
  if (result.status === 422) return 'Invalid order — price or size out of range';
  if (result.status === 429) return 'Rate limited — slow down';
  if (!result.error) return `Order failed (HTTP ${result.status})`;
  try {
    const body = JSON.parse(result.error);
    return body.error || body.message || result.error;
  } catch {
    return result.error;
  }
}
